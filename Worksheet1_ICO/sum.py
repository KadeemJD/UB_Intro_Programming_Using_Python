"""
Sum of Two Numbers
Write a program that prompts the user to enter two numbers. Compute their 
sum and display it.
"""

num1 = input("Please enter the first number: ")
num1 = int(num1)
num2 = input("Please enter the second number: ")
num2 = int(num2)
numSum = num1 + num2
print(f"The sum of your two numbers is {numSum}.")
