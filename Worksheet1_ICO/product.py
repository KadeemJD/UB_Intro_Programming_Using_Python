"""
Basic Product Calculation
Ask the user to enter two numbers. Calculate their product and print the result.
"""

num1 = input("Please enter the first number to be multiplied: ")
num1 = int(num1)
num2 = input("Please enter the second number to be multiplied: ")
num2 = int(num2)
product = num1 * num2
print(f"The product of your two numbers is {product}.")