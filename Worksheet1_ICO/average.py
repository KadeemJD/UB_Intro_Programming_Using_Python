"""
Average of Two Numbers
Prompt the user to enter two numbers. Calculate their average and print the result.
"""

num1 = input("Please enter the first number: ")
num1 = int(num1)
num2 = input("Please enter the second number: ")
num2 = int(num2)
average = (num1+num2)/2
print(f"The avaerage of your two numbers is {average}.")